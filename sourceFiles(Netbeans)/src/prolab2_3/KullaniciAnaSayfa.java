    private String sureyiFormatla(int sureInt) {
        // bu fonksiyon saniye cinsindeki sureyi saat:dakika:saniye olarak formatlar.
        String donusturulenSure = "";
        int dakika, saniye, saat;
        saat = sureInt / 3600;
        sureInt -= (saat * 3600);
        dakika = sureInt / 60;
        sureInt -= (dakika * 60);
        saniye = sureInt;
        //System.out.println(sure);
        //System.out.println(saat + " " + dakika +" "+ saniye);
        String saatStr = saat > 9 ? Integer.toString(saat) : "0" + Integer.toString(saat);
        String dakikaStr = dakika > 9 ? Integer.toString(dakika) : "0" + Integer.toString(dakika);
        String saniyeStr = saniye > 9 ? Integer.toString(saniye) : "0" + Integer.toString(saniye);
        donusturulenSure = saatStr + ":" + dakikaStr + ":" + saniyeStr;
        return donusturulenSure;
    }

