    private int metinOlarakVerilenSureyiDakikayaDonustur(String text) {
        int sonuc = 0;
        String parcalanmis[] = text.split(":");
        int saat = Integer.parseInt(parcalanmis[0]);
        int dakika = Integer.parseInt(parcalanmis[1]);
        int saniye = Integer.parseInt(parcalanmis[2]);

        sonuc = (saat * 3600) + (dakika * 60) + saniye;

        return sonuc;
    }


